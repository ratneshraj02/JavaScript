//Special character

// -> \n : Escape sequence - new 
console.log("The new apple \n iphone launched");

// -> \t - one tab
console.log('The new apple \t iphone launched');

// -> \f - form feed
console.log('"The new apple iphone launched"');

// -> \"    \" - Double quote
console.log('"The new apple iphone launched"');

// -> \'    \' - Single quote
console.log("\'The new apple iphone launched\'");

// -> \r - Carriage status
console.log('The new apple \r iphone launched');
