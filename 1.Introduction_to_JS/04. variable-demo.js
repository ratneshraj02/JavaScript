/*
    How to create Variables?
    There are three ways to create variable
        1.var
        2.let
        3.const
*/
//var <nameofvariable> = value
var marks = 90;
var score = 5;
console.log(marks);
console.log(score);

//let <nameofvariable> = value
let age = 19;
let flag = 0;
console.log(age);
console.log(flag);

//const <nameofvarible> =  value : This value can't we change any how.
const num = 12;
const num4 = 1234;
console.log(num);
console.log(num4);

