//Bit Wise Operator

/*
    These operator doesn't directly operate on number, but intead operator on binary reprenatation
    of number.
    
    bitwise AND  :  &
    bitwise OR   :  |
    bitwise XOR  :  ^
    bitwise NOT  :  ~

    Left Shift   :  <<
    Right Shift  :  >>

*/

//e.g : 
console.log(5 & 3);  //001 = 1

console.log(5 | 4);  //101 = 5

console.log(2 << 1);  //000010  <<  000100 = 100 = 4

console.log(2 >> 1);  //000010  >>  000100 = 001 = 1