/* Closures */
/*
    A closure is a function that has access to its own scope, the outer function's scope, and the global scope.

    It is created when a function is defined inside another function, allowing the inner function to "close over" the outer function's variables.   

    Closures are created every time a function is created, at function creation time.

    They allow for data encapsulation and can be used to create private variables.

    Closures can be used to create functions with private variables, which are not accessible from outside the function.

    They can also be used to create functions that remember their environment, which can be useful for creating functions that maintain state across multiple calls.
    
    Closures are often used in JavaScript for event handlers, callbacks, and other asynchronous operations
    where you need to maintain state or access variables from an outer scope.
    
    Before starting closures let's understand these topic.
   
*/

